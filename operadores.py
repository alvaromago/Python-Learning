## Operadores Aritméticos ##

print(3 + 5)
print(6 - 2)
print(4 * 3)
print(10 % 2)
print(2 ** 3) # 2 elevado a 3

print("Hola" + "Mundo") # HolaMundo
print("Hola", "Mundo") # Hola Mundo
# print("Hola" + 5) # Error!
print("Hola" * 3) # Hola Hola Hola

## Operadores Comparativos ##

print(3 > 5)
print(3 < 5)
print(3 >= 5)
print(3 <= 5)
print(3 == 5)
print(3 != 5)

print("Hola" > "Python") # Utilizados en Strings compara en base a orden alfabético (ASCII)
print("Hola" < "Python") 
print("Hola" >= "Python")
print("Hola" <= "Python")
print("Hola" == "Python")
print("Hola" != "Python")

## Operadores Lógicos ##

print(3 > 4 and "Hola" > "Python") # &&
print(3 > 4 or "Hola" > "Python") # ||
print(not(3 > 4)) # !=